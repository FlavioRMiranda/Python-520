#Operacoes entre numeros (int e Float)

a = 3.1415
b = 10

print (a + b)  # SOMA
print (a - b)  # SUBTRACAO
print (a * b)  # MULTIPLICACAO
print (a / b)  # DIVISAO
print (a % b)  # MODULO(RESTO DA DIVISAO)
print (a ** b) # EXPONENCIACAO

#Operacoes entre booleanos


x = True
y = False

print(x and y)
print(x or y)
print (not x)
print  (not y)

#Operacoes entre Strings

first_name = 'Flavio'
last_name = 'Miranda'

#Concatenacao

print(first_name + '' + last_name)

#Interpolacao
mensagem = 'Ola, {},seja bem vindo'.format(first_name)


print (mensagem)
